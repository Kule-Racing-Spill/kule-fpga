def create_smiley_bitmap(width, height):
    # Define the smiley face pattern
    # Using 0 for background and 1 for the smiley parts (eyes and mouth)
    smiley_pattern = [
        "00000000000000000000000000000000",
        "00000000000000000000000000000000",
        "00000000000000000000000000000000",
        "00000000000000000000000000000000",
        "00000000000011000000110000000000",
        "00000000000011000000110000000000",
        "00000000000000000000000000000000",
        "00000000000000000000000000000000",
        "00000000000000000000000000000000",
        "00000000000000000000000000000000",
        "00000011110000000000001111000000",
        "00000111111000000000011111100000",
        "00001111111100000001111111110000",
        "00001111111110000111111111100000",
        "00000111111111111111111111000000",
        "00000011111111111111111110000000",
        "00000001111111111111111100000000",
        "00000000111111111111111000000000",
        "00000000011111111111110000000000",
        "00000000001111111111100000000000",
        "00000000000111111111000000000000",
        "00000000000011111100000000000000",
        "00000000000000110000000000000000",
        "00000000000000000000000000000000",
        "00000000000000000000000000000000",
        "00000000000000000000000000000000",
        "00000000000000000000000000000000",
        "00000000000000000000000000000000",
        "00000000000000000000000000000000",
        "00000000000000000000000000000000",
        "00000000000000000000000000000000",
        "00000000000000000000000000000000"
    ]

    # Convert pattern to 4-bit values with spaces and newlines
    bitmap = ""
    for row in smiley_pattern:
        bitmap_row = " ".join(f"{'0011' if pixel == '0' else '0010'}" for pixel in row) + "\n"
        bitmap += bitmap_row

    return bitmap

# Create a 32x32 smiley bitmap sprite
smiley_bitmap = create_smiley_bitmap(32, 32)
